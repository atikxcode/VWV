//   description: 'Generated by create next app',
// }

export default function RootLayout({ children }) {
  const pathname = usePathname()
  const isAdminRoute = pathname.startsWith('/admin') // ðŸ‘ˆ detect admin

  return (
    <html lang="en">
      <body className={`${josefinSans.variable} antialiased bg-purple-50`}>
        <AuthProvider>
          {!isAdminRoute && <AgeVerification />}
          {!isAdminRoute && <Navbar />}
          {children}
          {!isAdminRoute && <Footer />}
        </AuthProvider>
      </body>
    </html>
  )
}
